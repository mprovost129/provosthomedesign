def populate_plan_content(apps, schema_editor):
    Plans = apps.get_model("plans", "Plans")
    HouseStyle = apps.get_model("plans", "HouseStyle")

    for plan_number, content in PLAN_CONTENT.items():
        plan = Plans.objects.filter(plan_number=plan_number).first()
        if not plan:
            continue

        changed_fields = []
        for field, value in content.items():
            if field in {"attributes", "styles"}:
                continue
            current_value = getattr(plan, field)
            is_generic_meta = (
                field == "meta_description"
                and str(current_value).strip().lower() == "house plan"
            )
            if not current_value or is_generic_meta:
                setattr(plan, field, value)
                changed_fields.append(field)

        for field, value in content.get("attributes", {}).items():
            if value and not getattr(plan, field):
                setattr(plan, field, True)
                changed_fields.append(field)

        if changed_fields:
            plan.save(update_fields=changed_fields)

        for style_slug in content.get("styles", ()):
            style = HouseStyle.objects.filter(slug=style_slug).first()
            if style:
                plan.house_styles.add(style)


